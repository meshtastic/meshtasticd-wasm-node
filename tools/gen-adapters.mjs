// Generate web/adapters.js from the firmware's canonical LoRa config YAMLs
// (<firmware>/bin/config.d/lora-*.yaml). We keep only CH341 USB adapters
// (`spidev: ch341`) — the only ones that can be driven over WebUSB — so the
// browser/headless chooser always matches what firmware actually supports.
//
//   FW=/path/to/firmware node tools/gen-adapters.mjs    (FW defaults to ../firmware)
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { parse as parseYaml } from "yaml";

const HERE = dirname(dirname(fileURLToPath(import.meta.url)));
const FW = process.env.FW || join(HERE, "..", "firmware");
const CONF = join(FW, "bin", "config.d");

// lora_module_enum (src/platform/portduino/PortduinoGlue.h) — YAML Module string -> enum int.
const MODULES = { sim: 0, auto: 1, RF95: 2, sx1262: 3, sx1268: 4, sx1280: 5, lr1110: 6, lr1120: 7, lr1121: 8, LLCC68: 9 };
// YAML pin key -> wasm_set_lora_pin name.
const PINS = { CS: "CS", IRQ: "IRQ", Reset: "RESET", Busy: "BUSY", RXen: "RXEN", TXen: "TXEN" };

function tcxoMv(v) {
  if (v === true) return 1800; // YAML `true` => firmware's default 1.8 V TCXO
  if (typeof v === "number") return v <= 10 ? Math.round(v * 1000) : v; // volts vs mV
  return 0;
}

const adapters = {};
for (const file of readdirSync(CONF).filter((f) => f.startsWith("lora-") && f.endsWith(".yaml"))) {
  let doc;
  try {
    doc = parseYaml(readFileSync(join(CONF, file), "utf8"));
  } catch {
    continue;
  }
  const L = doc?.Lora;
  if (!L || L.spidev !== "ch341") continue; // WebUSB needs a CH341 USB-to-SPI bridge
  if (!(L.Module in MODULES)) {
    console.warn(`skip ${file}: unknown Module ${L.Module}`);
    continue;
  }
  const pins = {};
  for (const [yk, n] of Object.entries(PINS)) if (typeof L[yk] === "number") pins[n] = L[yk];
  const id = file.replace(/^lora-/, "").replace(/\.yaml$/, ""); // unique per file
  adapters[id] = {
    id,
    label: doc.Meta?.name || id,
    support: doc.Meta?.support || "community",
    module: MODULES[L.Module],
    vid: L.USB_VID ?? 0x1a86,
    pid: L.USB_PID ?? 0x5512,
    dio2: L.DIO2_AS_RF_SWITCH ? 1 : 0,
    tcxo_mv: tcxoMv(L.DIO3_TCXO_VOLTAGE),
    spi_hz: typeof L.spiSpeed === "number" ? L.spiSpeed : 2000000,
    pins,
  };
}

const sorted = Object.fromEntries(
  Object.entries(adapters).sort(([, a], [, b]) =>
    a.support === b.support ? a.label.localeCompare(b.label) : a.support === "official" ? -1 : 1,
  ),
);

const out = `// AUTO-GENERATED by tools/gen-adapters.mjs from <firmware>/bin/config.d/lora-*.yaml
// (CH341 USB adapters only — driveable over WebUSB). Do not edit by hand.
// Regenerate: npm run gen:adapters

export const ADAPTERS = ${JSON.stringify(sorted, null, 2)};

// Apply a chosen adapter to the wasm node BEFORE wasm_setup(), via the
// wasm_set_lora_* exports. Pass null/undefined -> firmware's MeshToad default.
export function applyAdapter(Module, a) {
  if (!a) return;
  const C = (fn, types, args) => Module.ccall(fn, null, types, args);
  C("wasm_set_lora_module", ["number"], [a.module]);
  if (a.vid && a.pid) C("wasm_set_lora_usb_ids", ["number", "number"], [a.vid, a.pid]);
  C("wasm_set_lora_dio_config", ["number", "number"], [a.dio2 | 0, a.tcxo_mv | 0]);
  if (a.spi_hz) C("wasm_set_lora_spi_speed", ["number"], [a.spi_hz]);
  for (const [name, pin] of Object.entries(a.pins || {}))
    C("wasm_set_lora_pin", ["string", "number"], [name, pin]);
}
`;

writeFileSync(join(HERE, "web", "adapters.js"), out);
console.log(`wrote web/adapters.js — ${Object.keys(sorted).length} CH341 adapters: ${Object.keys(sorted).join(", ")}`);
